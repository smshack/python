print('중복을 허용하지 않음')
print('순서가 없음')
print('')
print('리스트나 튜플은 순서가 있기 때문에 인덱싱을 통해 자료형의 값을 얻을 수 있음')
print('사전 자료형과 집합 자료형은 순서가 없기 때문에 인뎃싱으로 값을 얻을 수 없음')
print('집합은 리스트 혹은 문자열을 이용해서 초기화 할 수 있음')
print('set 함수를 이용')
print('혹은 중괄호{} 안에 각 원소를 콤마(,)를 기준으로 구분하여 삽입함으로써 초기화 가능')
print('')
print('****************************************************')
print('-- 집합 자료형의 초기화 방법1')
data = set([1, 2, 3, 4, 4, 5, 5])
print(data)
print('')

print('-- 집합 자료형의 초기화 방법2')
data = {1, 1, 2, 3, 4, 4, 5, 5}
print(data)
print('')
print('----------------------------------------------')
print('***********집합자료형의 연산*************')
print('기본적인 집합 연산으로는 합집합, 교집합, 차집합 연산 등이 있음')
print('합집합: 집합 A 에 속하거나 B에 속하는 원소로 이루어진 집합(A ∪ B)')
print('교집합: 집합 A에도 속하고 B에도 속하는 원소로 이루어진 집합(A ∩ B)')
print('차집합: 집합 A 의 원소 중에서 B에 속하지 않는 원소들로 이루어진 집합(A - B)')

a = set([1, 2, 3, 4, 5])
b = set([3, 4, 5, 6, 7])

print('-- 합집합')
print(a | b)
print('')
print('-- 교집합')
print(a & b)
print('')
print('-- 차집합')
print(a - b)
print('')
print('----------------------------------------------')
print('***********집합자료형 관련 함수*************')

data = set([1, 2, 3])
