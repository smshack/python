apple = 17
banana = 3
print('더하기'+str(apple + banana))
print('빼기 '+str(apple - banana))
print('곱하기 '+str(apple * banana))
print('나누기 '+str(apple / banana))
print('몫'+str(apple // banana))
print('나머지'+str(apple % banana))
print('제곱'+str(apple ** banana))

